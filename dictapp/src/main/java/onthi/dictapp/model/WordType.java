package onthi.dictapp.model;

public class WordType {
    private int id;
    private String name;

    public WordType(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() { return id; }
    public String getName() { return name; }

    @Override
    public String toString() {
        return name; // Hiển thị tên trên ComboBox
    }
}
